//* var - let - const

//* var : Puede cambiar su valor y su scope es local
var genero = 'Masculino';
console.log(genero);

//* let: Puede cambiar su valor pero solo funcionara en el bloque donde fue declarada
let nombre;
nombre = "Juan";
console.log(nombre);

//* const: Una variable la cual NO puede cambiar su valor en el futuro
const apellido = "Perez";
console.log(apellido);